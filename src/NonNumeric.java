package datatype;

public class NonNumeric {
	public static void main(String[] args) {
		char c = 'a';
		System.out.println(c);
		char v = '2';
		System.out.println(v);
		char b = '%';
		System.out.println(b);
		boolean g = true;
		boolean h = false;
		System.out.println(g);
		System.out.println(h);
		
	
}

}
