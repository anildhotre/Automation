package datatype;

public class FloatingData {
	public static void main(String[] args) {
		float f =  1.123344f;
		System.out.println(f);
		float val1 = 6578548f;
		System.out.println(val1);
		double p = 675432.567489;
		System.out.println(p);
		double dd = 4;
		System.out.println(dd);
		
		int var22 = 88;
		double var222 = 575.45;
		System.out.println(var22);
		System.out.println(var222);
}

}
