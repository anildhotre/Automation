package operator;

public class ArithmaticOperator {public static void main(String[] args) {
	//addition
	int i = 10;
	int j= 50;
	int k = i+j;
	System.out.println(k);//60
	//substraction
	int l = i-j;
	System.out.println(l);//-40
	//multiplication
	int m = i*j;
	System.out.println(m);//500
	//division
	double f = 12;
	double g = 40;
	double d = f/g;
	System.out.println(d);//0.3
	//modulus'%'
	int h = 13;
	int p = 3;
	int w = h%p;
	System.out.println(w);//1
	

	
}
	
	

}
